"use client";

import {
  ColumnDef,
  flexRender,
  getCoreRowModel,
  useReactTable,
} from "@tanstack/react-table";

import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "./table";
import { useState, useEffect } from "react";

interface DataTableProps<TData, TValue> {
  columns: ColumnDef<TData, TValue>[];
  data: TData[];
}

export function DataTable<TData, TValue>({
  columns,
  data,
}: DataTableProps<TData, TValue>) {
  const table = useReactTable({
    data,
    columns,
    getCoreRowModel: getCoreRowModel(),
  });

  const [isSticky, setIsSticky] = useState(false);

  useEffect(() => {
    setTimeout(() => {
      const scrollFound = document.querySelector(
        "#scroll-transactions div[data-radix-scroll-area-viewport]",
      );
      if (!scrollFound) return;

      const handleScroll = () => {
        setIsSticky(scrollFound.scrollTop > 0);
      };
      scrollFound.addEventListener("scroll", handleScroll);

      return () => {
        scrollFound.removeEventListener("scroll", handleScroll);
      };
    }, 100);
  }, []);

  return (
    <div className="rounded-md border">
      <Table>
        <TableHeader
          className={`sticky top-0 bg-[#242020] shadow-md transition-all ${
            isSticky ? "h-[50px]" : "h-12"
          }`}
          data-sticky={isSticky}
        >
          {table.getHeaderGroups().map((headerGroup) => (
            <TableRow key={headerGroup.id}>
              {headerGroup.headers.map((header) => {
                return (
                  <TableHead key={header.id}>
                    {header.isPlaceholder
                      ? null
                      : flexRender(
                          header.column.columnDef.header,
                          header.getContext(),
                        )}
                  </TableHead>
                );
              })}
            </TableRow>
          ))}
        </TableHeader>
        <TableBody>
          {table.getRowModel().rows?.length ? (
            table.getRowModel().rows.map((row) => (
              <TableRow
                key={row.id}
                data-state={row.getIsSelected() && "selected"}
              >
                {row.getVisibleCells().map((cell) => (
                  <TableCell key={cell.id}>
                    {flexRender(cell.column.columnDef.cell, cell.getContext())}
                  </TableCell>
                ))}
              </TableRow>
            ))
          ) : (
            <TableRow>
              <TableCell colSpan={columns.length} className="h-24 text-center">
                No results.
              </TableCell>
            </TableRow>
          )}
        </TableBody>
      </Table>
    </div>
  );
}
